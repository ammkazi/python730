# functions 1. def 2. fn name 3. (two)list of paramaters 4. fn body
# defining a function (fn definition)
def addition(n1,n2):
    sum = n1+n2
    print(sum)

def findSquare(n):
    print(n*n)

def multiply(n1,n2):
    print(n1*n2)

# fn call - pass arguments
addition(10,20)
findSquare(100)
multiply(10,4)
